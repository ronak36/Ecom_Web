package com.pojo;

/**
 * @author Ronak
 *
 */
public class Accesory {

	private String accesoryId;
	private String accesoryName;
	private Double accesoryPrice;
	
	public String getAccesoryId() {
		return accesoryId;
	}
	public void setAccesoryId(String accesoryId) {
		this.accesoryId = accesoryId;
	}
	public String getAccesoryName() {
		return accesoryName;
	}
	public void setAccesoryName(String accesoryName) {
		this.accesoryName = accesoryName;
	}
	public Double getAccesoryPrice() {
		return accesoryPrice;
	}
	public void setAccesoryPrice(Double accesoryPrice) {
		this.accesoryPrice = accesoryPrice;
	}
	
	
}
